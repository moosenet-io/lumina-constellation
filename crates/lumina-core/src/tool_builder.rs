    // ── Test 2: parse_valid_json ──────────────────────────────────────────

    #[test]
    fn parse_valid_json() {
        let llm_output = r#"{
            "name": "check_url",
            "description": "Check if a URL returns HTTP 200",
            "argument_schema": {"type": "object", "properties": {"url": {"type": "string"}}},
            "capabilities": ["network", "stdout"],
            "source_code": "fn check_url(url: &str) -> bool { true }"
        }"#;

        let tool = ToolBuilder::parse_generation_response(llm_output);
        assert!(tool.is_some(), "Should parse valid JSON");
        let t = tool.unwrap();
        assert_eq!(t.name, "check_url");
        assert_eq!(t.capabilities, vec!["network", "stdout"]);
        assert!(!t.source_code.is_empty());
        assert!(!t.approved);
    }

    // ── Test 3: parse_invalid_returns_none ───────────────────────────────

    #[test]
