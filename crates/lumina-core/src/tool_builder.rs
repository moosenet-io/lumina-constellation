// Helpers
// ---------------------------------------------------------------------------

/// Validate that `name` is a safe, filesystem-friendly tool identifier.
///
/// Accepted pattern: `[a-z][a-z0-9_]{0,63}` — lowercase ASCII, starts with
/// a letter, at most 64 characters total.  This prevents path traversal
/// (`../../etc/...`), spaces, and other problematic characters.
fn is_valid_tool_name(name: &str) -> bool {
    if name.is_empty() || name.len() > 64 {
        return false;
    }
    let mut chars = name.chars();
    let first = match chars.next() {
        Some(c) => c,
        None => return false,
    };
    if !first.is_ascii_lowercase() {
        return false;
    }
    chars.all(|c| c.is_ascii_lowercase() || c.is_ascii_digit() || c == '_')
}

/// Convert capability name strings (e.g. `"network"`) to [`WasmCapability`] variants.
///
/// Unknown capability strings are silently ignored; callers should validate
/// capability names before submission if strict checking is needed.
fn capabilities_from_strings(caps: &[String]) -> Vec<WasmCapability> {
    caps.iter()
        .filter_map(|s| match s.as_str() {
            "stdout" => Some(WasmCapability::Stdout),
            "network" => Some(WasmCapability::Network { hosts: vec![] }),
            "filesystem" => Some(WasmCapability::Filesystem { paths: vec![] }),
            "env" => Some(WasmCapability::Env { keys: vec![] }),
            _ => None,
        })
        .collect()
}

/// Return a Unix epoch seconds string as a creation timestamp.
///
/// This avoids adding chrono as a hard dependency just for tool metadata.
fn unix_epoch_secs() -> String {
    use std::time::{SystemTime, UNIX_EPOCH};
    let secs = SystemTime::now()
        .duration_since(UNIX_EPOCH)
        .map(|d| d.as_secs())
        .unwrap_or(0);
    format!("{secs}")
}

