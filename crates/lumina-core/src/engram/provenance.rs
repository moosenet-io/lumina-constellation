    #[test]
    fn test_trace_source_returns_record() {
        let (store, path) = open_store("trace_source");

        let mut mem = Memory::new("user-ivan", MemoryType::Preference, SensitivityCategory::General, "prefers concise reports");
        mem.source_conversation_id = Some("conv-456".to_string());
        mem.source_turn_index = Some(7);
        mem.confidence = 0.9;
        store.insert_memory(&mem).unwrap();

        let record = ProvenanceTracker::trace_source(&store.conn, &mem.id).unwrap();
        assert!(record.is_some(), "trace_source should return a record");
        let record = record.unwrap();
        assert_eq!(record.memory_id, mem.id);
        assert_eq!(record.source_conversation_id.as_deref(), Some("conv-456"));
        assert_eq!(record.source_turn_index, Some(7));
        assert!((record.confidence - 0.9).abs() < 1e-5);

        let _ = std::fs::remove_file(&path);
    }

