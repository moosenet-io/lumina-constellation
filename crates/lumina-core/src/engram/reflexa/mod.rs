//! EMEM-06: Reflexa self-reflection engine for Engram v2.
//!
//! Runs weekly per-user via the scheduler. Reviews the memory space, detects
//! contradictions, consolidates duplicates, assesses quality, and triggers
//! principle refresh. All modifications are audited via eprintln!.
//!
//! Key design guarantees:
//! - Per-user isolation: NEVER accesses another user's memories.
//! - LLM failures are non-fatal: log warning, skip that phase, continue.
//! - This does NOT auto-delete — it marks superseded/flags for review only.
//! - All LLM calls use "lumina-fast" (local model, zero cost).
//! - Skip guard: users with < 10 total memories return an empty report.

pub mod consolidation;
pub mod contradiction;
pub mod quality;

use crate::chord::ChordClient;
use crate::config::Config;
use crate::engram::principles::PrincipleEngine;
use crate::engram::types::MemoryType;
use crate::engram::EngramStore;
use crate::error::Result;

/// Minimum number of memories a user must have before reflection runs.
pub const MIN_MEMORIES_FOR_REFLECTION: usize = 10;

// ── ReflexaEngine ──────────────────────────────────────────────────────────────

/// Weekly per-user reflection engine. Stateless — no persistent state between runs.
pub struct ReflexaEngine;

/// Summary of what the reflection cycle accomplished.
#[derive(Debug, Clone, Default)]
pub struct ReflectionReport {
    /// How many contradictions were detected and resolved via supersession.
    pub contradictions_resolved: usize,
    /// How many memories were consolidated (removed as near-duplicates).
    pub memories_consolidated: usize,
    /// How many memories were flagged with "quality_review_needed".
    pub quality_flags_raised: usize,
    /// How many new principles were generated by PrincipleEngine.
    pub principles_generated: usize,
    /// The user this report belongs to.
    pub user_id: String,
}

impl ReflexaEngine {
    /// Run full reflection for a user. Returns a ReflectionReport with all counts.
    ///
    /// Phases (in order):
    /// 1. Skip guard — if < MIN_MEMORIES_FOR_REFLECTION total → return empty report
    /// 2. Contradiction detection (Semantic memories only)
    /// 3. Consolidation (Semantic memories only)
    /// 4. Quality assessment (all types)
    /// 5. Principle refresh
    pub async fn run_reflection(
        store: &EngramStore,
        chord: &ChordClient,
        user_id: &str,
        config: &Config,
    ) -> Result<ReflectionReport> {
        // ── Phase 1: Skip guard ────────────────────────────────────────────────
        // Count all memories for this user (any type, including superseded ones).
        let total_count: i64 = store.conn.query_row(
            "SELECT COUNT(*) FROM memories_v2 WHERE user_id = ?1",
            rusqlite::params![user_id],
            |r| r.get(0),
        ).unwrap_or(0);

        if (total_count as usize) < MIN_MEMORIES_FOR_REFLECTION {
            eprintln!(
                "REFLEXA: skip_guard user={user_id} total_memories={total_count} \
                 threshold={MIN_MEMORIES_FOR_REFLECTION} result=skipped"
            );
            return Ok(ReflectionReport {
                user_id: user_id.to_string(),
                ..Default::default()
            });
        }

        eprintln!("REFLEXA: reflection_start user={user_id} total_memories={total_count}");

        let mut report = ReflectionReport {
            user_id: user_id.to_string(),
            ..Default::default()
        };

        // ── Phase 2: Contradiction detection ──────────────────────────────────
        // Load Semantic memories (already user_id-scoped by query_by_type).
        let semantic_memories = match store.query_by_type(MemoryType::Semantic) {
            Ok(mems) => mems,
            Err(e) => {
                eprintln!(
                    "REFLEXA: contradiction_phase_failed user={user_id} error={e} (non-fatal)"
                );
                Vec::new()
            }
        };

        // Per-user isolation: filter to only this user's memories (defence-in-depth).
        let user_semantics: Vec<_> = semantic_memories
            .into_iter()
            .filter(|m| m.user_id == user_id)
            .collect();

        match contradiction::detect_contradictions(store, chord, user_id, &user_semantics).await {
            Ok(resolved) => {
                report.contradictions_resolved = resolved;
                eprintln!(
                    "REFLEXA: contradiction_phase user={user_id} result={resolved}"
                );
            }
            Err(e) => {
                eprintln!(
                    "REFLEXA: contradiction_phase_failed user={user_id} error={e} (non-fatal)"
                );
            }
        }

        // ── Phase 3: Consolidation ─────────────────────────────────────────────
        match consolidation::merge_related_memories(store, user_id) {
            Ok(consolidated) => {
                report.memories_consolidated = consolidated;
                eprintln!(
                    "REFLEXA: consolidation_phase user={user_id} result={consolidated}"
                );
            }
            Err(e) => {
                eprintln!(
                    "REFLEXA: consolidation_phase_failed user={user_id} error={e} (non-fatal)"
                );
            }
        }

        // ── Phase 4: Quality assessment ────────────────────────────────────────
        match quality::flag_low_quality(store, user_id) {
            Ok(flagged) => {
                report.quality_flags_raised = flagged;
                eprintln!(
                    "REFLEXA: quality_phase user={user_id} result={flagged}"
                );
            }
            Err(e) => {
                eprintln!(
                    "REFLEXA: quality_phase_failed user={user_id} error={e} (non-fatal)"
                );
            }
        }

        // ── Phase 5: Principle refresh ─────────────────────────────────────────
        match PrincipleEngine::run_for_user(store, chord, user_id, config).await {
            Ok(new_ids) => {
                report.principles_generated = new_ids.len();
                eprintln!(
                    "REFLEXA: principle_phase user={user_id} result={}", new_ids.len()
                );
            }
            Err(e) => {
                eprintln!(
                    "REFLEXA: principle_phase_failed user={user_id} error={e} (non-fatal)"
                );
            }
        }

        eprintln!(
            "REFLEXA: reflection_complete user={user_id} \
             contradictions={} consolidated={} quality_flags={} principles={}",
            report.contradictions_resolved,
            report.memories_consolidated,
            report.quality_flags_raised,
            report.principles_generated,
        );

        Ok(report)
    }
}

