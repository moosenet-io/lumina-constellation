    fn test_key() -> Vec<u8> { vec![0u8; 32] }

    fn tmp_db(tag: &str) -> std::path::PathBuf {
        let p = std::path::PathBuf::from(format!("/tmp/lumina_reflexa_test_{tag}.db"));
        let _ = std::fs::remove_file(&p);
        p
    }

    fn insert_semantic(store: &EngramStore, user_id: &str, content: &str) {
        let m = Memory::new(user_id, MemoryType::Semantic, SensitivityCategory::General, content);
        store.insert_memory(&m).unwrap();
    }

    // ── test_reflexa_skip_guard_for_few_memories ──────────────────────────────

    #[test]
    fn test_reflexa_skip_guard_for_few_memories() {
        let path = tmp_db("skip_guard");
        let store = EngramStore::open(&path, &test_key()).unwrap();

        // Insert 9 memories — one below the threshold of 10
        for i in 0..(MIN_MEMORIES_FOR_REFLECTION - 1) {
            insert_semantic(&store, "system", &format!("fact {i}"));
        }

        let total: i64 = store.conn.query_row(
            "SELECT COUNT(*) FROM memories_v2 WHERE user_id = 'system'",
            [],
            |r| r.get(0),
        ).unwrap();
        assert!((total as usize) < MIN_MEMORIES_FOR_REFLECTION,
            "test setup: should have fewer than threshold memories");

        // Verify skip guard would trigger
        assert!((total as usize) < MIN_MEMORIES_FOR_REFLECTION,
            "skip guard should trigger for < {} memories, got {}",
            MIN_MEMORIES_FOR_REFLECTION, total);

        let _ = std::fs::remove_file(&path);
    }

    #[test]
    fn test_reflexa_skip_guard_returns_zero_counts() {
        let path = tmp_db("skip_guard_zeros");
        let store = EngramStore::open(&path, &test_key()).unwrap();

        // Only 5 memories — well below threshold
        for i in 0..5 {
            insert_semantic(&store, "system", &format!("fact {i}"));
        }

        // Simulate what run_reflection does for the skip guard
        let total_count: i64 = store.conn.query_row(
            "SELECT COUNT(*) FROM memories_v2 WHERE user_id = 'system'",
            [],
            |r| r.get(0),
        ).unwrap();

        // The report returned when skipped must have all zero counts
        let would_skip = (total_count as usize) < MIN_MEMORIES_FOR_REFLECTION;
        assert!(would_skip);

        // Verify the report shape
        let report = ReflectionReport {
            user_id: "system".to_string(),
            ..Default::default()
        };
        assert_eq!(report.contradictions_resolved, 0);
        assert_eq!(report.memories_consolidated, 0);
        assert_eq!(report.quality_flags_raised, 0);
        assert_eq!(report.principles_generated, 0);

        let _ = std::fs::remove_file(&path);
    }

