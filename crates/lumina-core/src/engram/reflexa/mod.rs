    // ── test_contradiction_resolution_supersedes_stale ────────────────────────

    #[test]
    fn test_contradiction_resolution_supersedes_stale() {
        let path = tmp_db("supersede_stale");
        let store = EngramStore::open(&path, &test_key()).unwrap();

        let mem_a = Memory::new("system", MemoryType::Semantic, SensitivityCategory::General, "I drink coffee");
        let mem_b = Memory::new("system", MemoryType::Semantic, SensitivityCategory::General, "I stopped drinking coffee");
        let id_a = mem_a.id.clone();
        let id_b = mem_b.id.clone();
        store.insert_memory(&mem_a).unwrap();
        store.insert_memory(&mem_b).unwrap();

        // Directly test the supersession mechanism
        contradiction::supersede_memory(&store, "system", &id_a, &id_b).unwrap();

        let superseded_by: Option<String> = store.conn.query_row(
            "SELECT superseded_by FROM memories_v2 WHERE id = ?1",
            params![id_a],
            |r| r.get(0),
        ).unwrap();

        assert_eq!(superseded_by.as_deref(), Some(id_b.as_str()),
            "superseded_by should point to the newer memory");

        // mem_b should NOT have superseded_by set
        let b_superseded: Option<String> = store.conn.query_row(
            "SELECT superseded_by FROM memories_v2 WHERE id = ?1",
            params![id_b],
            |r| r.get(0),
        ).unwrap();
        assert!(b_superseded.is_none(), "the newer memory should not be superseded");

        let _ = std::fs::remove_file(&path);
    }

    // ── test_consolidation_merges_related_memories ────────────────────────────

    #[test]
    fn test_consolidation_merges_related_memories() {
        let path = tmp_db("consolidation_merge");
        let store = EngramStore::open(&path, &test_key()).unwrap();

        // Near-identical embeddings will be consolidated
        let mut mem_a = Memory::new("system", MemoryType::Semantic, SensitivityCategory::General, "dark roast preference");
        mem_a.embedding = vec![1.0f32, 0.0];
        mem_a.created_at = "2026-01-01T00:00:00Z".to_string();
        mem_a.updated_at = "2026-01-01T00:00:00Z".to_string();

        let mut mem_b = Memory::new("system", MemoryType::Semantic, SensitivityCategory::General, "likes dark coffee");
        mem_b.embedding = vec![0.9999f32, 0.0001];
        // Normalise so cosine similarity > 0.80
        let norm: f32 = (0.9999f32 * 0.9999 + 0.0001 * 0.0001).sqrt();
        mem_b.embedding = vec![mem_b.embedding[0] / norm, mem_b.embedding[1] / norm];
        mem_b.created_at = "2026-06-01T00:00:00Z".to_string();
        mem_b.updated_at = "2026-06-01T00:00:00Z".to_string();

        store.insert_memory(&mem_a).unwrap();
        store.insert_memory(&mem_b).unwrap();

        let consolidated = consolidation::merge_related_memories(&store, "system").unwrap();
        assert!(consolidated >= 1, "should consolidate at least 1 near-duplicate pair");

        let remaining: i64 = store.conn.query_row(
            "SELECT COUNT(*) FROM memories_v2", [], |r| r.get(0)
        ).unwrap();
        assert_eq!(remaining, 1, "one of the near-duplicate pair should be removed");

        let _ = std::fs::remove_file(&path);
    }

