    // ── test_contradiction_detection_finds_opposing_memories ─────────────────

    #[test]
    fn test_contradiction_detection_finds_opposing_memories() {
        // Verify parse_contradiction_response handles all expected LLM outputs.
        assert_eq!(contradiction::parse_contradiction_response("CONTRADICT:A"), Some(0));
        assert_eq!(contradiction::parse_contradiction_response("CONTRADICT:B"), Some(1));
        assert_eq!(contradiction::parse_contradiction_response("NO_CONTRADICTION"), None);

        // Threshold check: near-identical embeddings should be above threshold
        let emb_a = vec![1.0f32, 0.0];
        let emb_b = vec![0.9999f32, 0.0001f32];
        let norm: f32 = (0.9999f32 * 0.9999 + 0.0001 * 0.0001).sqrt();
        let emb_b_norm = vec![emb_b[0] / norm, emb_b[1] / norm];
        let sim = crate::engram::cosine(&emb_a, &emb_b_norm);
        assert!(sim > contradiction::CONTRADICTION_SIMILARITY_THRESHOLD,
            "near-identical vectors should exceed contradiction threshold: {sim}");
    }

