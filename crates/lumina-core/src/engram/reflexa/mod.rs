    // ── test_per_user_isolation_in_reflection ─────────────────────────────────

    #[test]
    fn test_per_user_isolation_in_reflection() {
        // Verify quality flagging honours per-user boundaries.
        let path = tmp_db("isolation");
        let store = EngramStore::open(&path, &test_key()).unwrap();

        // Insert old low-confidence memories for both users
        for user in ["user-alice", "user-bob"] {
            for i in 0..5 {
                store.conn.execute(
                    "INSERT INTO memories_v2 (id, user_id, memory_type, visibility, sensitivity,
                        content, confidence, access_count, created_at, updated_at)
                     VALUES (?1, ?2, 'semantic', 'private', 'general', ?3, 0.3, 0,
                             '2020-01-01T00:00:00Z', '2020-01-01T00:00:00Z')",
                    params![
                        crate::engram::types::new_uuid(),
                        user,
                        format!("{user} fact {i}"),
                    ],
                ).unwrap();
            }
        }

        // Flag quality for alice only
        let flagged = quality::flag_low_quality(&store, "user-alice").unwrap();
        assert!(flagged > 0, "alice should have flagged memories");

        // Bob's memories should remain unflagged
        let bob_flags: i64 = store.conn.query_row(
            "SELECT COUNT(*) FROM memories_v2 WHERE user_id = 'user-bob' AND tags LIKE '%quality_review_needed%'",
            [],
            |r| r.get(0),
        ).unwrap();
        assert_eq!(bob_flags, 0, "bob's memories should not be flagged by alice's reflection pass");

        let _ = std::fs::remove_file(&path);
    }

